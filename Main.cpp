#include "Update.hpp"
#include "Draw.hpp"

Wnd wnd;
Image image;
Graph graph;

void Main()
{
	wnd = Wnd(640, 480);
	image = Image(wnd.w, wnd.h, Palette::Black);
	DynamicTexture texture(image);
	Window::Resize(wnd.w, wnd.h);
	Scene::SetBackground(ColorF{ 0.8, 0.9, 1.0 });

	graph = Graph(0, 0, 1.5, 1.5);



	while (System::Update())
	{
		Update();
		Draw();

		texture.fill(image);
		texture.draw();
	}
}

